package aprendizaje;

public class OperadoresIncrementoYDecremento {
    public static void main(String[] args) {
        int Num1, Num2;
        Num1 = 0; Num2 = 0;

        //INCREMENTO
        //Num1 = Num1+1;
        Num1 ++;
        System.out.println(Num1);
        
        //DECREMENTO
        Num2 --;
        System.out.println(Num1);
    }
}
